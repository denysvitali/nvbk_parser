package nvbk

import "fmt"

// LookupRFIDName returns a human-readable description of a sub-file RF ID byte
// by looking it up in the table extracted from liboemnvbk_img_helper.so.
// It returns the empty string when the RF ID is not present in the table.
func LookupRFIDName(rfid byte) string {
	if rfid == 0xff {
		return "manifest"
	}
	for _, e := range rfIDTable {
		if e.RFID == rfid {
			return fmt.Sprintf("hw=%d, rf=%d", e.HW, e.RF)
		}
	}
	return ""
}

// LookupNVItemName returns a human-readable name for a numeric NV item ID.
// The OEMNVBK images store only IDs in the compressed numeric format; names
// must be supplied externally. This function currently returns the empty
// string for unknown IDs, but the underlying map can be extended as more
// IDs are documented.
func LookupNVItemName(id uint16) string {
	return knownNVItemNames[id]
}

